#Asignación de Valores o Creación de Variables

#Existen 3 tipos de Datos: Numéricos, Textos, Booleanos

#NUMÉRICOS enteros (int)
numero = 10
print(numero)
print(type(numero))

#NUMÉRICOS decimales (float)
numero = 10.56
print(numero)
print(type(numero))

#TEXTOS (str)
cadena = 'a'
print(cadena)
print(type(cadena))

#comillas dentro de comillas
cadena = "Estoy 'estudiando'"
print(cadena)
print(type(cadena))

#BOOLEANOS (bool)
valor = True
print(valor)
print(type(valor))

valor = False
print(valor)
print(type(valor))

#Operaciones Aritméticas Básicas
num1 = 10
num2 = 6.7
resultado = (num1 + num2) * 10 /6
print("El resultado es: ", resultado)

#Tipado Dinámico
valor = 10
print(valor)

valor = "Sebastian"
print(valor)